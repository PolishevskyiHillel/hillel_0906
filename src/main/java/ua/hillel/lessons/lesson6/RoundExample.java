package ua.hillel.lessons.lesson6;

public class RoundExample {
    public static void main(String[] args) {

        double num = 5.3;

        System.out.println(Math.round(num)); // округляє до int
        System.out.println(Math.floor(num)); // округляє вниз
        System.out.println(Math.ceil(num)); // округляє вверх
    }
}
